SELECT MAX(DECODE(DEPTNO, 10, COUNT(*), 0)) AS "10번",
MAX(DECODE(DEPTNO, 20, COUNT(*), 0)) AS "20번",
MAX(DECODE(DEPTNO, 30, COUNT(*), 0)) AS "30번"
FROM EMP GROUP BY DEPTNO;

SELECT SUM(DECODE(DEPTNO, 10, 1, 0)) AS "10번",
SUM(DECODE(DEPTNO, 20, 1, 0)) AS "20번",
SUM(DECODE(DEPTNO, 30, 1, 0)) AS "30번"
FROM EMP ;